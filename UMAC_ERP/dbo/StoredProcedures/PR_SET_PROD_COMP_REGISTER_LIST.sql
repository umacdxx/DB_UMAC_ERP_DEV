		SELECT A.PROD_DT
			 , A.SET_COMP_CD AS SCAN_CODE
			 , MST_PRD.ITM_NAME_DETAIL AS ITM_NAME
			 , ISNULL(A.LOT_NO, '') AS LOT_NO
			 , A.COMP_QTY
			 , MST_USR.USER_NM
			FROM PD_SET_RESULT_COMP AS A
			INNER JOIN CD_PRODUCT_CMN AS MST_PRD ON A.SET_COMP_CD = MST_PRD.SCAN_CODE
			INNER JOIN TBL_USER_MST AS MST_USR ON A.IEMP_ID = MST_USR.USER_ID
		   WHERE A.PROD_DT BETWEEN @P_SET_PROD_SDATE AND @P_SET_PROD_EDATE
		     AND A.SET_PLAN_ID = @P_SET_PLAN_ID
			 AND A.RESTORE_YN = 'N'
			 AND A.COMP_QTY != 0
		   ORDER BY A.PROD_DT DESC, A.SCAN_CODE


		   -- 2024-08-22 아래 쿼리로 SUM 하려 했으나 등록자가 달라질 수 있어서 SUM 하지 않은 코드 사용
